import React, { useState } from "react";
import { useNavigate } from "react-router-dom";
import axios from "axios";
import theme from "../theme";

export default function JobMatch() {
  const navigate = useNavigate();
  const [jd, setJd] = useState("");
  const [loading, setLoading] = useState(false);
  const [result, setResult] = useState(null);

  const handleScan = async () => {
    if (!jd.trim()) return alert("Paste a Job Description first!");
    const user = JSON.parse(localStorage.getItem("user"));
    
    setLoading(true);
    setResult(null); // Reset previous result

    try {
      const res = await axios.post("http://localhost:5000/api/users/match-job", {
        userId: user.id,
        jobDescription: jd
      });
      setResult(res.data.result);
    } catch (err) {
      alert(err.response?.data?.message || "Scan failed.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div style={styles.page}>
      {/* Header */}
      <div style={styles.header}>
        <button onClick={() => navigate("/user-dashboard")} style={styles.backBtn}>‚Üê Back</button>
        <h2 style={styles.title}>üìë AI Job Matcher</h2>
      </div>

      <div style={styles.container}>
        {/* Left Side: Input */}
        <div style={styles.card}>
          <h3 style={{color: "#a78bfa", marginTop: 0}}>1. Paste Job Description</h3>
          <textarea
            value={jd}
            onChange={(e) => setJd(e.target.value)}
            placeholder="Paste the LinkedIn/Glassdoor job description here..."
            style={styles.textArea}
          />
          <button 
            onClick={handleScan} 
            disabled={loading}
            style={loading ? styles.disabledBtn : styles.scanBtn}
          >
            {loading ? "ü§ñ AI is Analyzing..." : "Analyze Match"}
          </button>
        </div>

        {/* Right Side: Results */}
        <div style={styles.card}>
          <h3 style={{color: "#34d399", marginTop: 0}}>2. Match Result</h3>
          
          {!result && !loading && (
            <p style={{color: "#666", fontStyle: "italic"}}>
              Results will appear here after analysis.
            </p>
          )}

          {loading && (
             <div style={styles.pulse}>Running AI Comparison...</div>
          )}

          {result && (
            <div style={{textAlign: "center"}}>
              {/* Score Circle */}
              <div style={styles.scoreCircle}>
                <span style={{fontSize: "32px", fontWeight: "bold", color: "white"}}>
                  {result.score}%
                </span>
                <span style={{fontSize: "12px", color: "#ccc"}}>MATCH</span>
              </div>

              {/* Advice Section */}
              <div style={{textAlign: "left", marginTop: "20px"}}>
                <h4 style={{marginBottom: "5px", color: "white"}}>AI Advice:</h4>
                <p style={{color: theme.colors.textSecondary, fontSize: "14px"}}>
                  {result.advice}
                </p>

                {result.missingSkills?.length > 0 && (
                  <>
                    <h4 style={{marginBottom: "5px", marginTop: "15px", color: "#f87171"}}>Missing Keywords:</h4>
                    <div style={{display: "flex", gap: "8px", flexWrap: "wrap"}}>
                      {result.missingSkills.map(skill => (
                        <span key={skill} style={styles.missingTag}>{skill}</span>
                      ))}
                    </div>
                  </>
                )}
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

const styles = {
  page: {
    minHeight: "100vh",
    padding: "40px",
    maxWidth: "1000px",
    margin: "0 auto",
  },
  header: {
    display: "flex",
    alignItems: "center",
    gap: "20px",
    marginBottom: "30px"
  },
  backBtn: {
    background: "transparent",
    border: "1px solid #444",
    color: "#ccc",
    padding: "8px 16px",
    borderRadius: "20px",
    cursor: "pointer"
  },
  title: {
    margin: 0,
    fontSize: "24px",
    color: "white"
  },
  container: {
    display: "grid",
    gridTemplateColumns: "1fr 1fr",
    gap: "20px",
    alignItems: "start"
  },
  card: {
    ...theme.glassPanel("20px"),
    padding: "25px",
    minHeight: "400px"
  },
  textArea: {
    width: "100%",
    height: "250px",
    background: "rgba(0,0,0,0.3)",
    border: "1px solid #444",
    borderRadius: "10px",
    color: "white",
    padding: "15px",
    fontSize: "14px",
    resize: "none",
    marginBottom: "15px"
  },
  scanBtn: {
    ...theme.button("primary"),
    width: "100%"
  },
  disabledBtn: {
    ...theme.button("primary"),
    width: "100%",
    opacity: 0.5,
    cursor: "not-allowed"
  },
  scoreCircle: {
    width: "120px",
    height: "120px",
    borderRadius: "50%",
    border: "4px solid #34d399",
    display: "flex",
    flexDirection: "column",
    alignItems: "center",
    justifyContent: "center",
    margin: "0 auto",
    boxShadow: "0 0 20px rgba(52, 211, 153, 0.3)"
  },
  missingTag: {
    background: "rgba(248, 113, 113, 0.2)",
    color: "#fca5a5",
    border: "1px solid rgba(248, 113, 113, 0.4)",
    padding: "4px 8px",
    borderRadius: "4px",
    fontSize: "12px",
    fontWeight: "bold"
  },
  pulse: {
    color: "#a78bfa",
    fontWeight: "bold",
    textAlign: "center",
    marginTop: "50px",
    animation: "pulse 1.5s infinite"
  }
};